    private void setUserOffline() {
        if(firebaseAuth.getCurrentUser()!=null ) {
            String userId = firebaseAuth.getCurrentUser().getUid();
            databaseReference.child(userId).child("connection").setValue("offline");
            goToLogin();
        }
    }

